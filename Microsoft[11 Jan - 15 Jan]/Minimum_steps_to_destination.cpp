class Solution{
public:
    int minSteps(int D){
        int pos=0, steps;
        for(steps=0; true;){
            pos+=steps;
            if(pos==D||(pos>D&&((pos-D)&1)==0)){
                return steps;
            }
            steps++;
        }
        return 0;
    }
};

